public class aula10 {

    public static void main(String[] args) {

        char c1 = 'a';
        System.out.println(c1); //a

        //ASCII Table
        int i1 = (int)c1;
        System.out.println(i1); //97

        char c2 = 'a' + 2;
        System.out.println(c2); //c

        char c3 = 49;
        System.out.println(c3); //1

        char c4 = (char)-14;
        System.out.println(c4);
    }
}
